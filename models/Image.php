<?php

class Image {
    private $id;
    private $title;
    private $url;

    public function getId() {
        return $this->id;
    }

    public function setId($id) {
        $this->id;
    }

    public function getTitle() {
        return $this->title;
    }

    public function setTitle($title) {
        $this->title;
    }

    public function getUrl() {
        return $this->url;
    }

    public function setUrl($url) {
        $this->url;
    } 
}